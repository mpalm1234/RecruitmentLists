
public class Sister {

	String name;
	int group;
	int spot;

	// Constructor:
	public Sister(String n, int g, int s) {
		this.name = n;
		this.group = g;
		this.spot = s;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public int getGroup() {
		return group;
	}

	public void setGroup(int group) {
		this.group = group;
	}
	
	public int getSpot() {
		return spot;
	}

	public void setSpot(int spot) {
		this.spot = spot;
	}


}
